package DuangClass;

// 影厅
public class Hall {
	// // 属性
	// 厅号
	private Integer hallNum;
	// 厅名
	private String hallName;

	// // 方法
	// set & get
	public Integer getHall_Num() {
		return hallNum;
	}

	public void setHall_Num(Integer hall_Num) {
		this.hallNum = hall_Num;
	}

	public String getHall_Name() {
		return hallName;
	}

	public void setHall_Name(String hall_Name) {
		this.hallName = hall_Name;
	}

}
